namespace NTComponents.Snackbar;

/// <summary>
///     Supported snackbar positions for the <see cref="NTSnackbar" /> component.
/// </summary>
public enum NTSnackbarPosition {

    /// <summary>
    ///     Positions the snackbar in the top-left corner.
    /// </summary>
    TopLeftCorner,

    /// <summary>
    ///     Positions the snackbar at the top center.
    /// </summary>
    CenterTop,

    /// <summary>
    ///     Positions the snackbar in the top-right corner.
    /// </summary>
    TopRightCorner,

    /// <summary>
    ///     Positions the snackbar in the bottom-left corner.
    /// </summary>
    BottomLeftCorner,

    /// <summary>
    ///     Positions the snackbar at the bottom center.
    /// </summary>
    BottomCenter,

    /// <summary>
    ///     Positions the snackbar in the bottom-right corner.
    /// </summary>
    BottomRightCorner
}
