namespace NTComponents;

/// <summary>
///     Specifies where a minimized <see cref="NTWindow" /> is docked in the viewport.
/// </summary>
public enum NTWindowDockPosition {
    /// <summary>
    ///     Docks the minimized window at the top-left edge of the viewport.
    /// </summary>
    TopLeft,

    /// <summary>
    ///     Docks the minimized window at the top-center edge of the viewport.
    /// </summary>
    TopCenter,

    /// <summary>
    ///     Docks the minimized window at the top-right edge of the viewport.
    /// </summary>
    TopRight,

    /// <summary>
    ///     Docks the minimized window at the bottom-left edge of the viewport.
    /// </summary>
    BottomLeft,

    /// <summary>
    ///     Docks the minimized window at the bottom-center edge of the viewport.
    /// </summary>
    BottomCenter,

    /// <summary>
    ///     Docks the minimized window at the bottom-right edge of the viewport.
    /// </summary>
    BottomRight
}
