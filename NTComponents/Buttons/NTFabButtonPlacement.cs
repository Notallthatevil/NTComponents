namespace NTComponents;

/// <summary>
///     Defines how an <see cref="NTFabButton" /> is positioned.
/// </summary>
public enum NTFabButtonPlacement {

    /// <summary>
    ///     Renders the FAB inline in normal document flow.
    /// </summary>
    Inline,

    /// <summary>
    ///     Fixes the FAB to the lower-right corner of the viewport.
    /// </summary>
    LowerRight,

    /// <summary>
    ///     Fixes the FAB to the lower-left corner of the viewport.
    /// </summary>
    LowerLeft,

    /// <summary>
    ///     Fixes the FAB to the upper-right corner of the viewport.
    /// </summary>
    UpperRight,

    /// <summary>
    ///     Fixes the FAB to the upper-left corner of the viewport.
    /// </summary>
    UpperLeft
}
