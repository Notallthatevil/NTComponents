namespace NTComponents;

/// <summary>
///     Represents the preferred NTComponents theme mode.
/// </summary>
public enum NTTheme {

    /// <summary>
    ///     Resolve the active theme from the user's system preference.
    /// </summary>
    System,

    /// <summary>
    ///     Use the light theme.
    /// </summary>
    Light,

    /// <summary>
    ///     Use the dark theme.
    /// </summary>
    Dark
}
